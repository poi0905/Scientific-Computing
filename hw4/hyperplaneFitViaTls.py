d = int(input())
data = []
data.append(d)
for i in range(data[0]):
    temp = [float(val) for val in input().split()]
    data.append(temp)

def hyperplaneFitViaTls(data):
    from numpy.linalg import eig
    import numpy as np

    d = data[0]
    n = len(data[1])
    avg = [0] * d
    for i in range(d):
        for j in range(n):
            avg[i] += data[1+i][j] / n
    for i in range(d):
        for j in range(n):
            data[1+i][j] = data[1+i][j] - avg[i]
    data.remove(data[0])
    A = np.array(data)
    U = np.dot(A, A.T)
    e_val, e_vec = np.linalg.eig(U)
    e_vec = e_vec.T
    a = e_val.tolist().index(min(e_val))    # why min?
    constant = 0
    ans = []
    for i in range(d):
        constant += e_vec[a][i] * avg[i]
        ans.append(e_vec[a][i])
    if ans[0] < 0:
        for i in range(d):
            ans[i] = -ans[i]
        ans.append(constant)
    else:
        ans.append(-constant)
    ans = np.array(ans)
    print(ans)


if __name__ == '__main__':
    hyperplaneFitViaTls(data)


